<script setup>
import { computed } from 'vue';
import { Link } from '@inertiajs/vue3';

const props = defineProps({
    href: {
        type: String,
        required: true,
    },
    active: {
        type: Boolean,
    },
});

const classes = computed(() =>
    props.active
        ? 'inline-flex items-center px-1 pb-1.5 border-b-2 border-white text-sm font-semibold leading-5 text-white focus:outline-none transition duration-150 ease-in-out'
        : 'inline-flex items-center px-1 pb-1.5 border-b-2 border-transparent text-sm font-medium leading-5 text-slate-400 hover:text-white focus:outline-none transition duration-150 ease-in-out',
);
</script>

<template>
    <Link :href="href" :class="classes">
        <slot />
    </Link>
</template>
